    private void jButtonImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImprimirActionPerformed
        File fich = new File(jTextFieldFichero.getText());
		if (fich.exists()) {
                    FileReader fichfr = null;
            try {
                fichfr = new FileReader(fich);
                BufferedReader fichBR = new BufferedReader(fichfr);
                int contador = 0;
                String linea;
                char c;
                if ( (int)(c = (char) fichfr.read()) != -1) {
                    System.out.print(c);
                    while ((linea = fichBR.readLine()) != null) {
                        System.out.println(linea + "\n");
                        contador++;
                    }
                    jLabelContador.setText("NÃºmero de lineas: " + contador);
                }else {
                    System.out.println("El archivo esta vacio.");
                }
                fichBR.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Clase_3.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Clase_3.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    fichfr.close();
                } catch (IOException ex) {
                    Logger.getLogger(Clase_3.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
		} else {
			System.out.println("El fichero no existe.");
		}
    }//GEN-LAST:event_jButtonImprimirActionPerformed

