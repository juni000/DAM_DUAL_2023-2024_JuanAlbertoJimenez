    private void jMenuItemAdicionarDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemAdicionarDepActionPerformed
        //ventana para selecionar archivo
        JFileChooser ventanaFich = new JFileChooser();
        int opc = ventanaFich.showOpenDialog(null);
        if (opc == ventanaFich.APPROVE_OPTION) {
            try {
                BufferedReader fichBR = new BufferedReader(new FileReader(ventanaFich.getSelectedFile()));
                
                String linea = fichBR.readLine();
                if (linea.isBlank()) {
                    jLabelResultado.setText("Esta Vacio el fichero de entrada");
                    jLabelResultado.setVisible(true);
                }else {
                    //conectamos con la base de datos
                    //Conectar a la BD
                    Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost:3306/empleados?serverTimezone=UTC", "root", "root");

                    // La consulta es un String con c√≥digo SQL 
                    String sql = "insert into departamento set nombre=?,presupuesto=?,gastos=?";
                    
                    // Creamos la clase PreparedStatement para poder hacer consultas 
                    PreparedStatement consulta= conexion.prepareStatement(sql);

                    while (linea!=null) {                        
                        String campos[] = linea.split(";");
                        consulta.setString(1, campos[0]);
                        consulta.setDouble(2, Double.parseDouble(campos[1]));
                        consulta.setDouble(3, Double.parseDouble(campos[2]));
                        
                        //ejecutar consulta
                        consulta.executeUpdate();
                        linea = fichBR.readLine();
                    }
                    jLabelResultado.setText("Registros adicionados");
                    jLabelResultado.setVisible(true);
                    consulta.close();
                    fichBR.close();
                }
                
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItemAdicionarDepActionPerformed

